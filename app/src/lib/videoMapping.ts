// Helper function to get video by card name
export function getVideoByCardName(cardName: string): { url: string; embedUrl: string } | null {
  // Exact match first
  if (cardVideos[cardName]) {
    return cardVideos[cardName];
  }
  
  // Try normalized name (remove extra spaces, handle special characters)
  const normalizedName = cardName.replace(/\s+/g, ' ').trim();
  if (cardVideos[normalizedName]) {
    return cardVideos[normalizedName];
  }
  
  // Try without trademark symbols
  const cleanName = cardName.replace(/[®™©]/g, '').trim();
  if (cardVideos[cleanName]) {
    return cardVideos[cleanName];
  }
  
  return null;
}
